const $blockNews = $("#blockNews")

function createBlockNews() {
  for (const news of arrNews) {
    const $div = $("<div>").addClass("news")
    const $h2 = $("<h2>").text(news.title)
    const $text = $("<p>").addClass("text").text(news.description)

    const $attributes = $("<div>").addClass("attributes")
    const $date = $("<p>").text(news.created_at)
    const $a = $("<a>").attr("href", "news.html")
    const $input = $("<input>").attr({
      type: "button",
      value: "Детальніше"
    })

    $a.append($input)
    $attributes.append($date, $a)
    $div.append($h2, $text, $attributes)
    $blockNews.append($div)
  }
}


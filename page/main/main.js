let count = 0
const $textNews = $("#textNews")
const $slider = $("#slider")
const $pagination = $("#pagination")

const $h2 = $("<h2>")
const $p = $("<p>")

function updateNewsForSlider(index) {
  count = index
  $h2.text(newsForSlider[count].title)
  $p.text(newsForSlider[count].description)
  $slider.css("background-image", `url(${newsForSlider[count].img_url})`)

  $pagination.empty()

  for (let i = 0; i < newsForSlider.length; i++) {
    const $dot = $("<div>").addClass("dot")
    if (i === count) {
      $dot.addClass("active")
    }
    $pagination.append($dot)
  }

  $textNews.empty().append($h2, $p)
}

$("#left").on("click", function () {
  updateNewsForSlider((count - 1 + newsForSlider.length) % newsForSlider.length)
})

$("#right").on("click", function () {
  updateNewsForSlider((count + 1) % newsForSlider.length)
})

updateNewsForSlider(count);
setInterval(() => {
  updateNewsForSlider((count + 1) % newsForSlider.length)
}, 8000)

